package com.example.RequestParamSample07.form;

public class SampleForm {
    private String name;
    private int age;
    private String birth;
    
    // Getter
    public String getName() {
        return name;
    }
    
    public int getAge() {
        return age;
    }
    
    public String getBirth() {
        return birth;
    }
    
    // Setter
    public void setName(String name) {
        this.name = name;
    }
    
    public void setAge(int age) {
        this.age = age;
    }
    
    public void setBirth(String birth) {
        this.birth = birth;
    }
}
